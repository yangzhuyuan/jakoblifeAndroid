// 获取公历year年第n个节气的公历日期
function getTerm(year, n) {
	if (year < 1900 || year > 2100) { return -1 }
	if (n < 1 || n > 24) { return -1 }

	const value = solarTermInfo[year - 1900]
	const day = []
	
	for (var i = 0; i <= 5; i++) {
		const str = parseInt('0x' + value.substr(i*5, 5)).toString()
		day.push(str.substr(0, 1))
		day.push(str.substr(1, 2))
		day.push(str.substr(3, 1))
		day.push(str.substr(4, 2))
	}

	return parseInt(day[n - 1])
}

/**
 * 获取date日期属于哪个节气
*/
function getSolarTerm(date) {
	const targetDate = date ? new Date(date) : new Date()
	const year = targetDate.getFullYear()
	const month = targetDate.getMonth()+1
	const day = targetDate.getDate()
	
	// 当月节气几号开始
	const startDay = getTerm(year, (month * 2 - 1))
	// 当月节气几号结束
	const endDay = getTerm(year, (month * 2))
	let solarTermName = null

	if (startDay === day) {
		solarTermName = solarTerms[month * 2 - 2]
	}

	if (endDay === day) {
		solarTermName = solarTerms[month * 2 - 1]
	}
	
	return solarTermName
}

